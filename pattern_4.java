public class pattern_4
{
    public static void main(String args[])
    {
        int i,j,k=65;
        for(i=5;i<=1;i--)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print((char)k);
                k++;
            }
            System.out.println();
        }
    }
}
            
        
        
        