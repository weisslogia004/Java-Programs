public class pattern_8
{
    public static void main(String args[])
    {
        int i,j;
        for(i=9;i>=1;i=i-2)
        {
            for(j=9;j>=1;j=j-2)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
        